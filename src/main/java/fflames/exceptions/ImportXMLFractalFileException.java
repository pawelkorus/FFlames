package fflames.exceptions;

public class ImportXMLFractalFileException extends Exception {
	private static final long serialVersionUID = 5505268769493127939L;

	public ImportXMLFractalFileException() {
		super();
	}

	public ImportXMLFractalFileException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ImportXMLFractalFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImportXMLFractalFileException(String message) {
		super(message);
	}

	public ImportXMLFractalFileException(Throwable cause) {
		super(cause);
	}

}
